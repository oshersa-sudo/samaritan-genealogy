def build_succession(prefix, rows, tag):
    """Linear deputy-succession: each holder linked to his predecessor."""
    persons = []
    for i, (given, label) in enumerate(rows):
        pid = '%s%d' % (prefix, i + 1)
        p = {"id": pid, "name": '%s · %s %d' % (given, tag, i + 1), "sex": "M"}
        if label: p["note"] = label
        if i > 0: p["father_id"] = '%s%d' % (prefix, i)
        persons.append(p)
    for i in range(len(persons) - 1):
        persons[i]['children_ids'] = [persons[i + 1]['id']]
    return persons

