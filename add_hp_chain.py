def build_chain(prefix, rows):
    persons, by_given = [], {}
    prev_pid = None
    for i, (given, patro, label) in enumerate(rows):
        pid = '%s%d' % (prefix, i + 1)
        # tag the Shechem succession with its ordinal so the many repeated names
        # (אלעזר/עקבון/נתנאל…) are distinguishable in the tree; Aaron is the apex.
        disp = given if (i == 0 or prefix != 'שכם') else ('%s · כה״ג %d' % (given, i))
        p = {"id": pid, "name": disp, "sex": "M"}
        if label: p["note"] = label
        if patro.startswith('@'):    # Damascus/outsider: father not in-list -> succession
            fid = prev_pid
        else:
            fid = by_given.get(patro) if patro else None
            if patro and not fid:    # patronymic not found earlier -> succession to predecessor
                fid = prev_pid
        if fid: p["father_id"] = fid
        persons.append(p)
        by_given[given] = pid
        prev_pid = pid
    cm = {}
    for p in persons:
        if p.get('father_id'): cm.setdefault(p['father_id'], []).append(p['id'])
    for p in persons:
        if p['id'] in cm: p['children_ids'] = cm[p['id']]
    return persons

